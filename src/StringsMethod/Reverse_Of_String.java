package StringsMethod;

public class Reverse_Of_String {
	public static void main(String[] args) {
		String s="Rajesh";
		
	String rev = "";
	
	for(int i=s.length()-1;i>=0;i--)
	{
	    rev= rev+s.charAt(i);             // ithe rev= 0 ahe ani rev+ charAt(i) madhe h yenr coz apn s.lenght ghetl je shevtpryt aste tya;a reverse kel
	  
	
	
	}
	System.out.println(rev);
	}

}
